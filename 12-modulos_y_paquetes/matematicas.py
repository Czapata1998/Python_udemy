#Modulo de matematicas en python

import math

print(f"Raiz cuadrada de 10 {math.sqrt(10)}") #Funcion para sacar raiz cuadrada


print(f"Numero pi tiene el valor de {float(math.pi)}") # Sacar el numero pi


print(f"redondear {math.ceil(6.56789)}") #Eta funcion se usa para redondear cifras hacia arriba

print(f"redondear {math.floor(6.56789)}") #Lo redondea hacia abajo

#Modulo random para sacara aleatoreos

import random
